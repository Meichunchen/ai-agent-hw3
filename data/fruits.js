// 把一筆水果資料轉成適合 embedding 的文字
export function fruitToText(fruit) {
  return [
    `名稱：${fruit.name}`,
    `產地：${fruit.region}`,
    `盛產季節：${fruit.season}`,
    `品種：${fruit.varieties.join("、")}`,
    `特色：${fruit.description}`,
  ].join("。");
}
